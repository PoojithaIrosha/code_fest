    public void searchDrivers(String text) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            
            ResultSet driverRs = MySQL.search("SELECT * FROM driver INNER JOIN gender ON driver.gender_id = gender.id WHERE `email` LIKE '%"+text+"%'");
            
            while(driverRs.next()) {
                Vector<String> v = new Vector<>();
                v.add(driverRs.getString("driver.nic"));
                v.add(driverRs.getString("driver.name"));
                v.add(driverRs.getString("driver.email"));
                v.add(driverRs.getString("driver.contact_no"));
                v.add(driverRs.getString("driver.address"));
                v.add(driverRs.getString("gender.gender"));
                dtm.addRow(v);
            }
            
            jTable1.setModel(dtm);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
