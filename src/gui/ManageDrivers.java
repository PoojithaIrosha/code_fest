    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String nic = jTextField1.getText();
        String name = jTextField4.getText();
        String email = jTextField5.getText();
        String contact = jTextField6.getText();
        String address = jTextField7.getText();
        String gender = jComboBox1.getSelectedItem().toString();

        if (nic.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter driver's nic", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter driver's name", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter driver's email", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (contact.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter driver's contact number", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(contact).matches()) {
            JOptionPane.showMessageDialog(this, "Contact Number Invalid", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter driver's address", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please enter driver's gender", "Message", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet driverRs = MySQL.search("SELECT * FROM `driver` WHERE `nic` = '" + nic + "'");

                if (driverRs.next()) {
                    JOptionPane.showMessageDialog(this, "Driver has already registered", "Message", JOptionPane.WARNING_MESSAGE);
                } else {

                    ResultSet genderRs = MySQL.search("SELECT * FROM `gender` WHERE `gender` = '" + gender + "'");
                    genderRs.next();
                    String genderId = genderRs.getString("id");

                    MySQL.iud("INSERT INTO `driver`(`nic`, `name`, `email`, `contact_no`, `address`, `gender_id`) VALUES ('" + nic + "', '" + name + "', '" + email + "', '" + contact + "', '" + address + "', '" + genderId + "')");

                    clearFields();
                    loadDrivers();
                    JOptionPane.showMessageDialog(this, "New driver registered successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

