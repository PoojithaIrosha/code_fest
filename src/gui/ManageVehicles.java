    public void searchVehicles(String text) {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            ResultSet vehicleRs = MySQL.search("SELECT * FROM vehicle INNER JOIN make ON vehicle.make_id = make.id INNER JOIN vehicle_type ON vehicle.vehicle_type_id = vehicle_type.id INNER JOIN `status` ON  vehicle.status_id=`status`.id WHERE `reg_no` LIKE '%" + text + "%'");

            while (vehicleRs.next()) {
                Vector<String> v = new Vector<>();
                v.add(vehicleRs.getString("vehicle.reg_no"));
                v.add(vehicleRs.getString("vehicle_type.name"));
                v.add(vehicleRs.getString("make.name"));
                v.add(vehicleRs.getString("vehicle.model"));
                v.add(vehicleRs.getString("vehicle.cost_per_km"));
                v.add(vehicleRs.getString("status.name"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

