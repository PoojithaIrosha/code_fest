    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        String regNo = jTextField1.getText();
        String vType = jComboBox2.getSelectedItem().toString();
        String make = jComboBox1.getSelectedItem().toString();
        String model = jTextField2.getText();
        String cost = jTextField3.getText();

        System.out.println(make);

        if (regNo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter vehicle registration number", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (vType.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select vehicle type", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (make.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select make of vehicle", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (model.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter model of the vehicle", "Message", JOptionPane.WARNING_MESSAGE);
        } else if (cost.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the cost per KM", "Message", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet vehicleRs = MySQL.search("SELECT * FROM `vehicle` WHERE `reg_no` = '" + regNo + "'");
                if (vehicleRs.next()) {
                    JOptionPane.showMessageDialog(this, "This vehicle is already registered", "Message", JOptionPane.WARNING_MESSAGE);
                } else {

                    ResultSet makeRs = MySQL.search("SELECT * FROM `make` WHERE `name` = '" + make + "'");
                    makeRs.next();
                    String makeId = makeRs.getString("id");
                    System.out.println(makeId);

                    ResultSet vTypeRs = MySQL.search("SELECT * FROM `vehicle_type` WHERE `name` = '" + vType + "'");
                    vTypeRs.next();
                    String vTypeId = vTypeRs.getString("id");

                    MySQL.iud("INSERT INTO `vehicle`(`reg_no`, `model`, `vehicle_type_id`, `cost_per_km`, `make_id`) VALUES ('" + regNo + "', '" + model + "', '" + vTypeId + "', '" + cost + "', '" + makeId + "')");

                    clearFields();
                    loadVehicles();

                    JOptionPane.showMessageDialog(this, "New vehicle registered successfully", "Message", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton3ActionPerformed

